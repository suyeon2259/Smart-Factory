package com.path.version;

public class VersionDto {
	private String ver_id;
	private String ver_title;
	private String ver_version;
	private String ver_content;
	private String ver_image;
	private String ver_date;
	
	public String getVer_date() {
		return ver_date;
	}
	public void setVer_date(String ver_date) {
		this.ver_date = ver_date;
	}
	public String getVer_id() {
		return ver_id;
	}
	public void setVer_id(String ver_id) {
		this.ver_id = ver_id;
	}
	public String getVer_title() {
		return ver_title;
	}
	public void setVer_title(String ver_title) {
		this.ver_title = ver_title;
	}
	public String getVer_version() {
		return ver_version;
	}
	public void setVer_version(String ver_version) {
		this.ver_version = ver_version;
	}
	public String getVer_content() {
		return ver_content;
	}
	public void setVer_content(String ver_content) {
		this.ver_content = ver_content;
	}
	public String getVer_image() {
		return ver_image;
	}
	public void setVer_image(String ver_image) {
		this.ver_image = ver_image;
	}
}
